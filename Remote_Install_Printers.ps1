function SortListView
{
    param([parameter(Position=0)][UInt32]$Column)
    
    $Numeric = $true # determine how to sort
    
    if($Script:LastColumnClicked -eq $Column)
    {
        $Script:LastColumnAscending = -not $Script:LastColumnAscending
    }
    else
    {
        $Script:LastColumnAscending = $true
    }
    $Script:LastColumnClicked = $Column
    $ListItems = @(@(@())) 
    
    foreach($ListItem in $listview_Printers.Items)
    {
        if($Numeric -ne $false) 
        {
            try
            {
                $Test = [Double]$ListItem.SubItems[[int]$Column].Text
            }
            catch
            {
                $Numeric = $false 
            }
        }
        $ListItems += ,@($ListItem.SubItems[[int]$Column].Text,$ListItem)
    }
    
    $EvalExpression = {
        if($Numeric)
        { return [Double]$_[0] }
        else
        { return [String]$_[0] }
    }
    
    $ListItems = $ListItems | Sort-Object -Property @{Expression=$EvalExpression; Ascending=$Script:LastColumnAscending}
    
    $listview_Printers.BeginUpdate()
    $listview_Printers.Items.Clear()
    foreach($ListItem in $ListItems)
    {
        $listview_Printers.Items.Add($ListItem[1])
    }
    $listview_Printers.EndUpdate()
}

function UpdateValues {
    $Global:PrintersLocation = $ComboBoxLocation.text
    $Global:DestinationComputer = $ComboBoxChooseComputer.text
    $Global:DestinationHostName = $TextBoxComputerName.Text

    if($Global:DestinationComputer -eq 'Local - this computer'){
        $Global:Destination = 'Local'
        $TextBoxComputerName.enabled = $false
    }
    elseif($Global:DestinationComputer -eq "Send directory of current location's print server to client"){
        $Global:Destination = "Remote List"
        $TextBoxComputerName.enabled = $true
    }
    elseif($Global:DestinationComputer -eq "Remotely add selected printer(s)"){
        $Global:Destination = "Remote Add Printers"
        $TextBoxComputerName.enabled = $true
    }
}

function UpdateLocation {
    UpdateValues

    $currentprintserver = $printservers | Where-Object {$_.PrintersLocation -eq $Global:PrintersLocation}
    
    $Global:Location = $currentprintserver.PrintServerAddress 
    GetPrinters($Global:Location)


       
}


Function GetPrinters{
    $LabelPrintersNumber.Text = 'Searching for Printer(s) in '+ $Global:PrintersLocation + '...'
    $listview_Printers.Items.Clear()
    $listview_Printers.Columns.Clear()

    $global:Printers = Get-Printer -ComputerName $Global:Location | Select Name,Location,PortName,Comment,DriverName,PrinterStatus
   
    $PrinterProperties = $Printers[0].psObject.Properties
 
    $PrinterProperties | ForEach-Object {
        $listview_Printers.Columns.Add("$($_.Name)") | Out-Null
    }
 
    ForEach ($Printer in $Printers){
        $PrinterListViewItem = New-Object System.Windows.Forms.ListViewItem($Printer.Name)
 
        $Printer.psObject.Properties | Where-Object {$_.Name -ne "Name"} | ForEach-Object {
            $ColumnName = $_.Name
            $PrinterListViewItem.SubItems.Add("$($Printer.$ColumnName)") | Out-Null
            if($PrinterListViewItem.SubItems.Text -Like 'Error*'){
                
                $PrinterListViewItem.BackColor ='#ff7979'

            }
            if($PrinterListViewItem.SubItems.Text -Like 'Offline*'){
                
                $PrinterListViewItem.BackColor ='#ffaf40'

            }
            if($PrinterListViewItem.SubItems.Text -Like 'TonerLow*'){
                
                $PrinterListViewItem.BackColor ='#fffa65'
            }

            if($PrinterListViewItem.SubItems.Text -Like '*NoToner*'){
                
                $PrinterListViewItem.BackColor ='#F8EFBA'

            }
            if($PrinterListViewItem.SubItems.Text -Like 'PaperJam*'){
                
                $PrinterListViewItem.BackColor ='#cd84f1'

            }            
        }
 
        $listview_Printers.Items.Add($PrinterListViewItem) | Out-Null
 
    }
 
    $listview_Printers.AutoResizeColumns("HeaderSize")
    $PrinterNumber = $global:Printers.Count
    $LabelPrintersNumber.Text = 'There are '+$PrinterNumber+' printers in '+ $Global:PrintersLocation
}

Function AddPrinter{
    UpdateValues

        if($Global:Destination -eq 'Local'){
            $SelectedPrinters = @($listview_Printers.SelectedIndices)
    
            $IdColumnIndex = ($listview_Printers.Columns | Where-Object {$_.Text -eq "Name"}).Index
            
            $SelectedPrinters | ForEach-Object {
    
                $PrintersName = ($listview_Printers.Items[$_].SubItems[$IdColumnIndex]).Text
                
                Write-host "Script is Adding printer:$PrintersName on local computer"  -ForegroundColor Green
                $ConnectionName = "$Global:Location\$PrintersName"
                Write-Host 

                Add-Printer -connectionname $ConnectionName 
            }
        }
        elseif($Global:Destination -eq "Remote List"){
            
            Write-host "Script is opening folder from printserver $Global:Location on:$Global:DestinationHostName" -ForegroundColor Green
            $argument = "/c psexec.exe -sid \\$Global:DestinationHostName explorer.exe $Global:Location"
            Write-Host $argument
            Start-Process cmd.exe $argument

        }
        elseif($Global:Destination -eq "Remote Add Printers"){

            $SelectedPrinters = @($listview_Printers.SelectedIndices)
            $IdColumnIndex = ($listview_Printers.Columns | Where-Object {$_.Text -eq "Name"}).Index
            $IdColumnIndex2 = ($listview_Printers.Columns | Where-Object {$_.Text -eq "PortName"}).Index 
            $IdColumnIndex3 = ($listview_Printers.Columns | Where-Object {$_.Text -eq "DriverName"}).Index
            $IdColumnIndex4 = ($listview_Printers.Columns | Where-Object {$_.Text -eq "Comment"}).Index
            $IdColumnIndex5 = ($listview_Printers.Columns | Where-Object {$_.Text -eq "Location"}).Index
    
            $SelectedPrinters | ForEach-Object {

                Try {
                    
                    $PrintersName = ($listview_Printers.Items[$_].SubItems[$IdColumnIndex]).Text
                    $PortName = ($listview_Printers.Items[$_].SubItems[$IdColumnIndex2]).Text
                    $DriverName = ($listview_Printers.Items[$_].SubItems[$IdColumnIndex3]).Text
                    $Comment = ($listview_Printers.Items[$_].SubItems[$IdColumnIndex4]).Text
                    $Location = ($listview_Printers.Items[$_].SubItems[$IdColumnIndex5]).Text
                    Write-host "Script is Adding printer:"$PrintersName  -ForegroundColor Green
                    Write-Host $PrintersName $PortName $DriverName $Comment $Location
                    Add-PrinterPort -Name $PortName -ComputerName $Global:DestinationHostName -ErrorAction SilentlyContinue
                    Add-Printer -ComputerName $Global:DestinationHostName -Name $PrintersName -DriverName $DriverName -PortName $PortName -Comment $Comment -Location $Location -ErrorAction Stop
                    Start-Sleep -Seconds 2
                    }
                Catch {
                    Write-host "2nd Attempt Script is Adding remotely printer:$PrintersName to: $Global:DestinationHostName" -ForegroundColor Green
                    Start-Sleep -Seconds 2
                        
                    $argument = "/c psexec.exe -sid \\$Global:DestinationHostName explorer.exe $Global:Location\$PrintersName"
                    Write-Host $argument
                    Start-Process cmd.exe $argument
            
                }            
            }               
         } 
}

Function AddPrinter2{
    UpdateValues

    $RestricDrvrInstallToAdminOff1 = "powershell.exe -ExecutionPolicy ByPass New-Item -Path 'HKLM:\Software\Policies\Microsoft\Windows NT\Printers' -Name PointAndPrint"
    $RestricDrvrInstallToAdminOff2 = "powershell.exe -ExecutionPolicy ByPass Set-ItemProperty -Path 'HKLM:\Software\Policies\Microsoft\Windows NT\Printers\PointAndPrint' -Name RestrictDriverInstallationToAdministrators -Value 0 -type DWORD"

    $RestrictDrvrInstallToAdminOn = "powershell.exe -windowstyle hidden Set-ItemProperty -Path 'HKLM:\Software\Policies\Microsoft\Windows NT\Printers\PointAndPrint' -Name RestrictDriverInstallationToAdministrators -Value 1 -type DWORD"

